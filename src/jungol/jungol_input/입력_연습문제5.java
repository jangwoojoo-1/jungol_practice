package jungol.jungol_input;

public class 입력_연습문제5 {
    public static void main(String[] args) {
        double x, y;
        x = 1.2340;
        y = 10.34590;

        System.out.print("전체 7자리로 맞추고 소수 4자리까지 출력\n");
        System.out.printf("x = %7.4f\ny = %7.4f\n\n", x, y);
        System.out.print("소수 2자리까지 출력\n");
        System.out.printf("x = %.2f\ny = %.2f\n\n", x, y);
    }
}
