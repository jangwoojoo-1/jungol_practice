package jungol.jungol_loop2;

public class 반복제어문2_연습문제7 {
    public static void main(String[] args) {
        for(int i = 1 ; i<=5 ; i++){
            for(int j = 1 ; j<=5 ; j++){
                System.out.printf("%d ", i);
            }
            System.out.println();
        }
        System.out.println();
        for(int i = 1 ; i<=5 ; i++){
            for(int j = 1 ; j<=5 ; j++){
                System.out.printf("%d ", j);
            }
            System.out.println();
        }
    }
}
